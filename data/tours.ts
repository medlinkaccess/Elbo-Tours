export function getTourBySlug(slug: string): Tour | undefined {
  return tours.find(t => t.slug === slug);
}

export function getToursByType(type: 'multiday' | 'excursion'): Tour[] {
  return tours.filter(t => t.type === type);
}

export function getToursByDeparture(city: string): Tour[] {
  return tours.filter(t => t.departsFrom.some(d => d.toLowerCase().includes(city.toLowerCase())));
}

export function getToursByTag(tag: string): Tour[] {
  return tours.filter(t => t.tags.some(tg => tg.toLowerCase() === tag.toLowerCase()));
}
